use sipha::prelude::*;
use std::sync::OnceLock;

static BUILT: OnceLock<BuiltGraph> = OnceLock::new();

/// Lazily-built grammar graph for this crate.
///
/// The returned graph is immutable: call this once (it initializes on first use), keep the
/// reference, and pass it to [`crate::parse::parse_doc_with_built`] and related APIs when parsing
/// many sources so you avoid repeating this lookup in tight loops (the cost is small either way).
///
/// Enable Cargo feature `grammar-v4-only` to specialize the graph for [`crate::parse::Version::V4`]
/// / experimental flags only (fewer flag checks in generated bytecode).
pub fn built_graph() -> &'static BuiltGraph {
    BUILT.get_or_init(|| {
        let mut g = GrammarBuilder::new();
        g.allow_rule_cycles(true);
        g.set_trivia_rule("trivia");

        stmt::define(&mut g);
        tokens::define(&mut g);
        r#type::define(&mut g);
        expr::define(&mut g);

        g.finish().expect("leekscript grammar should build")
    })
}
